package rewriting;

public class DATA {

	int address;
	String info;
	
	public DATA (int address, String info) {
		this.address = address;
		this.info = info;
	}
	
	
	public int getAddress() {
		return address;
	}
	
	public String getInfo() {
		return info;
	}
	
	public void setAddress(int address) {
		this.address = address;
	}
	
	public void setInfo(String info) {
		this.info = info;
	}
	
	
}
